export class FlightDataModel {

    public get id(): string {
        return this.id;
    }

    public set id(id: string) {
        this.id = id;
    }

    public get providerName(): string {
        return this.providerName;
    }

    public set providerName(providerName: string) {
        this.providerName = providerName;
    }

    public get providerCode(): string {
        return this.providerCode;
    }

    public set providerCode(providerCode: string) {
        this.providerCode = providerCode;
    }

    public get providerType(): string {
        return this.providerType;
    }

    public set providerType(providerType: string) {
        this.providerType = providerType;
    }

}
